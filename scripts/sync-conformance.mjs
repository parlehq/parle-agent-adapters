#!/usr/bin/env node
// Vendors the core-owned conformance fixtures (parlehq/parle #457) into
// packages/client/conformance, verifies them against the core manifest,
// records the pin, and regenerates src/conformance-data.ts. Bumping the pin
// by re-running this script against a newer core ref is the explicit act of
// adopting new server behavior (ADR-0061).
//
// Usage: node scripts/sync-conformance.mjs
//   PARLE_CORE_CHECKOUT  path to a parlehq/parle checkout (default ../parle)
//   PARLE_CORE_REF       git ref to vendor from (default origin/main)
import { execFileSync } from "node:child_process";
import { createHash } from "node:crypto";
import { mkdirSync, readFileSync, rmSync, writeFileSync } from "node:fs";
import { dirname, join, resolve } from "node:path";
import { fileURLToPath } from "node:url";

const repoRoot = resolve(dirname(fileURLToPath(import.meta.url)), "..");
const clientDir = join(repoRoot, "packages/client");
const vendorRoot = join(clientDir, "conformance");

export function sha256(bytes) {
  return createHash("sha256").update(bytes).digest("hex");
}

export function renderConformanceData(versionFixture, tokenClassesFixture) {
  const version = JSON.parse(versionFixture).parle_version;
  const tokens = JSON.parse(tokenClassesFixture);
  return [
    "// Generated by scripts/sync-conformance.mjs from packages/client/conformance.",
    "// Do not edit; re-run the sync script to adopt a new core fixture pin.",
    `export const CONFORMANCE_PARLE_VERSION = ${JSON.stringify(version)};`,
    "",
    "export type ConformanceTokenClass = {",
    "  name: string;",
    "  prefix: string;",
    "  secret: boolean;",
    "  shape: string;",
    "  redaction_pattern: string;",
    "  redact_with: string;",
    "  description: string;",
    "  examples: Array<{ input: string; expected: string }>;",
    "};",
    "",
    `export const CONFORMANCE_TOKEN_CLASSES: ConformanceTokenClass[] = ${JSON.stringify(tokens.token_classes, null, 2)};`,
    "",
  ].join("\n");
}

function main() {
  const core = resolve(repoRoot, process.env.PARLE_CORE_CHECKOUT || "../parle");
  const ref = process.env.PARLE_CORE_REF || "origin/main";
  const coreSha = execFileSync("git", ["-C", core, "rev-parse", ref], { encoding: "utf8" }).trim();
  const versionDirs = execFileSync("git", ["-C", core, "ls-tree", "-d", "--name-only", ref, "conformance/"], { encoding: "utf8" })
    .trim().split("\n").filter(Boolean);
  if (versionDirs.length !== 1) throw new Error(`expected exactly one conformance version dir, got: ${versionDirs.join(", ") || "none"}`);
  const versionDir = versionDirs[0];

  rmSync(vendorRoot, { recursive: true, force: true });
  mkdirSync(vendorRoot, { recursive: true });
  const archive = execFileSync("git", ["-C", core, "archive", ref, versionDir], { maxBuffer: 64 * 1024 * 1024 });
  execFileSync("tar", ["-x", "-C", vendorRoot, "--strip-components=1"], { input: archive });

  const vendored = join(vendorRoot, versionDir.split("/").pop());
  const manifest = JSON.parse(readFileSync(join(vendored, "manifest.json"), "utf8"));
  const files = {};
  for (const fixture of manifest.fixtures) {
    const bytes = readFileSync(join(vendored, fixture.path));
    const digest = sha256(bytes);
    if (digest !== fixture.sha256) throw new Error(`hash mismatch for ${fixture.path}: manifest ${fixture.sha256}, vendored ${digest}`);
    files[fixture.path] = digest;
  }
  files["manifest.json"] = sha256(readFileSync(join(vendored, "manifest.json")));

  const pin = {
    core_ref: coreSha,
    parle_version: manifest.parle_version,
    fixture_schema_version: manifest.fixture_schema_version,
    files,
  };
  writeFileSync(join(clientDir, "conformance.pin.json"), JSON.stringify(pin, null, 2) + "\n");

  const data = renderConformanceData(
    readFileSync(join(vendored, "version.json"), "utf8"),
    readFileSync(join(vendored, "token-classes.json"), "utf8"),
  );
  writeFileSync(join(clientDir, "src/conformance-data.ts"), data);
  console.log(`pinned core ${coreSha.slice(0, 7)} parle_version ${manifest.parle_version} schema ${manifest.fixture_schema_version}`);
}

if (process.argv[1] === fileURLToPath(import.meta.url)) main();
